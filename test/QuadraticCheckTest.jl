function test_get_fixed_vars()
    @testset "get_fixed_vars unit tests" begin
        dim_1 = 1
        @test get_fixed_vars(dim_1) == []

        dim_2 = 2
        @test get_fixed_vars(dim_2) == [(1,),(2,)]

